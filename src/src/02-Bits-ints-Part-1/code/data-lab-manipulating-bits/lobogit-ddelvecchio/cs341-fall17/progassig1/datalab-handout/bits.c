/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /*
   * Masks the positive/negative sign bit and &s together with ~x
   * then takes the ~mask and &s with x. Adding them together gives
   * the highest bit. Then you add n with ~0 to get the shift and
   * finally shift it over
   */
  int w;
  int y = x >> 31;
  int z = ~x;
  z = z & y;
  y = ~y;
  w = x & y;
  x = w + z;
  w = n + ~0;
  x = x >> w;
  x =!x;
  return x;
  
}
