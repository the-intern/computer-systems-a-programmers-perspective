int logicalShift(int x, int n) {
/*
 *First records and removes the sign bit. Then it shifts the number
 *by n. Then lines up and returns the sign bit to the proper place.
 *
 */
  int y = x>>31;
  int z = 0;
  y=y<<31;
  x=x^y;
  x=x>>n;
  y=y>>n;
  z=y<<1;
  y=y^z;
  return x|y;
}
